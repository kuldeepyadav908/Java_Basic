package StringClass;

public class Lab487 {

	public static void main(String[] args) {
		String str = "JLCINDIA";
		System.out.println(str.substring(0));		//JLCINDIA
		System.out.println(str.substring(3));		//INDIA
		System.out.println(str.substring(0,4));		//JLCI
		System.out.println(str.substring(3,5));		//IN
	}
}
